public class HelloWorld {

	public static void main(String[] args) {
        // This is a simple Java program that prints "Hello World!" to the console.
		
		//print()	Prints text without a new line at the end.EX-System.out.print("Hi") → Hi
		//println()	Prints text with a new line at the end.EX- System.out.println("Hi") → Hi\n
		//printf()	Prints formatted text (like in C). Doesn't add a new line by default.EX-System.out.printf("Hello %s", "John") → Hello John
		
		System.out.print("Hello World! ");
		System.out.println("Hello World! "); // ln = next line
		System.out.printf("Hello World! "); // print ,printf ,println
	}
}